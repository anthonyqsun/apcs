public class Date {
    public static void main(String[] args) {
        String day = "Friday";
        int date = 17;
        String month = "August";
        int year = 2021;
        System.out.println("American format:");
        System.out.print(day);
        System.out.print(", ");
        System.out.print(month);
        System.out.print(" ");
        System.out.print(date);
        System.out.print(", ");
        System.out.println(year);
        System.out.println("European format:");
        System.out.print(day);
        System.out.print(" ");
        System.out.print(month);
        System.out.print(" ");
        System.out.print(date);
        System.out.print(" ");
        System.out.println(year);
    }
}
